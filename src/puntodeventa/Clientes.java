    private void addClienteNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addClienteNuevoActionPerformed
      JTextField nombre = new JTextField(20);
      JTextField direccion = new JTextField(20);
      JTextField correo = new JTextField(20);
      JTextField edad = new JTextField(2);
      JTextField rfc = new JTextField(20);

      JPanel myPanel = new JPanel();
      myPanel.setLayout(new BoxLayout(myPanel, BoxLayout.Y_AXIS));
      myPanel.add(new JLabel("Nombre: "));
      myPanel.add(nombre);
      
      myPanel.add(new JLabel("Direcci칩n: "));
      myPanel.add(direccion);
      
      myPanel.add(new JLabel("Correo: "));
      myPanel.add(correo);
      
      myPanel.add(new JLabel("Edad:"));
      myPanel.add(edad);
      
      myPanel.add(new JLabel("RFC: "));
      myPanel.add(rfc);

      int result = JOptionPane.showConfirmDialog(null, myPanel, 
               "Ingresa datos de cliente", JOptionPane.OK_CANCEL_OPTION);
      if (result == JOptionPane.OK_OPTION) {
            Cliente c = new Cliente(nombre.getText(), direccion.getText(), correo.getText(),parseInt(edad.getText()),rfc.getText());
            EscribirArchivo.addCliente(c);
            c.facturar();
            JOptionPane.showMessageDialog(null, "Se enviar치 factura");
            this.dispose();
      }

    }//GEN-LAST:event_addClienteNuevoActionPerformed

